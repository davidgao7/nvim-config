return {
    {
        'nvimdev/dashboard-nvim',
        event = 'VimEnter',
        dependencies = { { 'nvim-tree/nvim-web-devicons' } },
        config = function()
            -- randomly choose a logo from 1 to 7
            local logo_list = { logo1, logo2, logo3, logo4, logo5, logo6, logo7 }
            -- Seed the random number generator (important for true randomness)
            math.randomseed(os.time())

            -- Randomly select an index
            local random_index = math.random(1, #logo_list)

            -- Get the random value
            local logo = logo_list[random_index]
            logo = string.rep("\n", 8) .. logo .. "\n\n"

            local options = {
                header = vim.split(logo, "\n"),

                -- stylua: ignore
                -- TODO: transfer to fzf-lua
                center = {
                    { action = function() require("fzf-lua").files() end, desc = " Find File", icon = " ", key = "f" },
                    { action = function() require("fzf-lua").oldfiles() end, desc = " Recent Files", icon = " ", key = "r" },
                    { action = function() require("fzf-lua").live_grep() end, desc = " Find Text", icon = " ", key = "g" },
                    {
                        action = function()
                            require("fzf-lua").files({
                                prompt = "Find NVIM Config> ",
                                cwd = vim.fn.expand("~/.config/nvim/lua/"), -- Set the root directory
                            })
                        end,
                        desc = " Config",
                        icon = " ",
                        key = "c"
                    },
                    { action = "Lazy", desc = " Lazy", icon = "󰒲 ", key = "l" },
                    { action = function() vim.api.nvim_input("<cmd>qa<cr>") end, desc = " Quit", icon = " ", key = "q" },
                },

                footer = function()
                    local stats = require("lazy").stats()
                    local ms = (math.floor(stats.startuptime * 100 + 0.5) / 100)
                    return {
                        "⚡ Neovim loaded " .. stats.loaded .. "/" .. stats.count .. " plugins in " .. ms .. "ms",
                    }
                end,
            }

            require('dashboard').setup {
                theme = "doom",
                hide = {
                    -- this is taken care of by lualine
                    -- enabling this messes up the actual laststatus setting after loading a file
                    statusline = false,
                },
                config = options,

            }

            for _, button in ipairs(options.center) do
                button.desc = button.desc .. string.rep(" ", 43 - #button.desc)
                button.key_format = "  %s"
            end

            -- close Lazy and re-open when the dashboard is ready
            if vim.o.filetype == "lazy" then
                vim.cmd.close()
                vim.api.nvim_create_autocmd("User", {
                    pattern = "DashboardLoaded",
                    callback = function()
                        require("lazy").show()
                    end,
                })
            end

            return options
        end,
    },
}
